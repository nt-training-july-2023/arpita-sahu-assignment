package JavaBasics;

public class IncrementDecrement {
	public static void main(String[] args) {
		int var = 5;
		System.out.println(++var); //becomes 6 and print 6
		System.out.println(var++); //becomes 7 but print 6
		System.out.println(--var); //becomes 6 and print 6
		System.out.println(var--); //becomes 5 but print 6
		System.out.println(var); //print 5

	}

}
